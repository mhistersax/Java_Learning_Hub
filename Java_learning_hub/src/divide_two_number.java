public class divide_two_number {
    public static void main(String[] args) {
            // Input numbers to be added
        int num1 = 100;
        int num2 = 20;
        
        // Calculate the sum
        int sum = num1 / num2;
        
        // Display the result
        System.out.println("The division of " + num1 + " and " + num2 + " is: " + sum);
    }
}
